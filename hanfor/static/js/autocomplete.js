/**
 * Search a term
 * @param term
 */
function fuzzy_search(term) {
    return fuse.search(term);
}

/**
 * filter to be used in js filter method.
 * @param value
 * @param index
 * @param ar
 * @returns {boolean}
 */
function fuzzy_filter(value, index, ar) {
    return this.includes(index);
}

